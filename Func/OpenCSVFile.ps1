<#-----------------------------------------------------------------------------
[OverView]
-----------------------------------------------------------------------------#>
<#
　CSVファイルを開いてCSVオブジェクトを返す関数。
　　・引数1:取得するCSVファイルの絶対パス

#>
<#-----------------------------------------------------------------------------
[1. Define]
-----------------------------------------------------------------------------#>
<#-------------------------------------
[1-1. 引数取得]
-------------------------------------#>
if ($args[0]) {$CSVFile = $args[0]} else {$CSVFile = Read-Host "取得するCSVFileの絶対パスを入力して下さい。"}

<#-----------------------------------------------------------------------------
[2. GetSrc]
-----------------------------------------------------------------------------#>
<#-------------------------------------
[2-1. CSVデータ取得]
-------------------------------------#>
if (Test-Path $CSVFile) {$CSVData = Get-Content $CSVFile|ConvertFrom-CSV}

<#-----------------------------------------------------------------------------
[4. PostDist]
-----------------------------------------------------------------------------#>
return $CSVData

exit


